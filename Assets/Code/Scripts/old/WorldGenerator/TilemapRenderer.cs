    // Method to generate the tilemap
    void Start()
    {
        GenerateTilemap();
    }

    void GenerateTilemap()
    {
        for (int x = 0; x < tilemapData.GetLength(0); x++)
        {
            for (int y = 0; y < tilemapData.GetLength(1); y++)
            {
                int tileIndex = tilemapData[x, y]; // Get tile index from tilemap data
                Vector2 uvStart = GetUVStart(tileIndex); // Get UV start coordinates for the tile

                // Create a quad for the tile
                GameObject tile = GameObject.CreatePrimitive(PrimitiveType.Quad);
                tile.transform.parent = transform;
                tile.transform.position = new Vector3(x * tileSize, y * tileSize, 0);

                // Set UV coordinates for the quad to render the corresponding tile from the texture
                MeshRenderer meshRenderer = tile.GetComponent<MeshRenderer>();
                meshRenderer.material = new Material(Shader.Find("Standard"));
                meshRenderer.material.mainTexture = tilesetTexture;
                meshRenderer.material.mainTextureScale = new Vector2(0.5f, 0.5f); // Assuming 2x2 grid
                meshRenderer.material.mainTextureOffset = uvStart;
            }
        }
    }

    // Method to calculate UV start coordinates for a given tile index
    Vector2 GetUVStart(int tileIndex)
    {
        float tileX = (tileIndex % (tilesetTexture.width / tileSize)) * 0.5f; // Assuming 2x2 grid
        float tileY = 1.0f - ((tileIndex / (tilesetTexture.height / tileSize)) * 0.5f); // Assuming 2x2 grid
        return new Vector2(tileX, tileY);
    }
